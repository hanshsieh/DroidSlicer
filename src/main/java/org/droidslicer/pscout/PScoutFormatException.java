package org.droidslicer.pscout;

public class PScoutFormatException extends Exception
{
	private static final long serialVersionUID = 3461568281146800318L;
	public PScoutFormatException()
	{
		super();
	}
	public PScoutFormatException(String s)
	{
		super(s);
	}
	public PScoutFormatException(Throwable cause)
	{
		super(cause);
	}
}
